const gTTS = require('gtts');
const path = require('path');
const fs = require('fs');

const audioDir = path.join(__dirname, '..', 'public', 'audio');
if (!fs.existsSync(audioDir)) {
    fs.mkdirSync(audioDir, { recursive: true });
}

const text = `กรมอุตุฯ เตือน ลานีญา จ่อถล่มไทย เตรียมรับมือฝนชุกกว่าปกติช่วงปลายปี
กรุงเทพมหานคร กรมอุตุนิยมวิทยาออกประกาศเฝ้าระวังปรากฏการณ์ ลานีญา ที่กำลังก่อตัวขึ้นในมหาสมุทรแปซิฟิก โดยคาดการณ์ว่าประเทศไทยจะเริ่มได้รับผลกระทบชัดเจนตั้งแต่เดือนสิงหาคมนี้เป็นต้นไป ส่งผลให้ปริมาณฝนสะสมทั่วประเทศสูงกว่าค่าเฉลี่ยปกติร้อยละ 10 ถึง 15 ดร.สมชาย ผู้เชี่ยวชาญด้านสภาพภูมิอากาศ ระบุว่าปรากฏการณ์นี้จะทำให้อุณหภูมิผิวน้ำทะเลบริเวณแปซิฟิกตะวันตกสูงขึ้น ส่งผลให้ความชื้นพัดเข้าสู่เอเชียตะวันออกเฉียงใต้มากขึ้น สิ่งที่น่ากังวลไม่ใช่แค่ปริมาณฝน แต่คือความถี่ของพายุหมุนเขตร้อนที่อาจเคลื่อนตัวเข้าสู่ไทยมากกว่าปีที่ผ่านมา ซึ่งอาจส่งผลกระทบต่อพื้นที่เกษตรกรรมในภาคกลางและภาคตะวันออกเฉียงเหนือ ทั้งนี้ รัฐบาลได้สั่งการให้หน่วยงานบริหารจัดการน้ำเร่งระบายน้ำในเขื่อนหลัก เพื่อเตรียมพื้นที่รองรับมวลน้ำใหม่ และขอให้ประชาชนติดตามข่าวสารอย่างใกล้ชิดเพื่อป้องกันความเสียหายที่อาจเกิดขึ้นกับทรัพย์สินและพืชผลทางการเกษตร`;

const filename = 'l3_listen_news.mp3';
const filepath = path.join(audioDir, filename);

console.log(`Generating ${filename}...`);
const gtts = new gTTS(text, 'th');
gtts.save(filepath, function (err, result) {
    if(err) { 
        console.error(`Error saving ${filename}:`, err);
        process.exit(1);
    }
    console.log("Success! saved " + filename);
});
